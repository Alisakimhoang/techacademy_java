
public class ArithmeticOperations2 {

    public static void main(String[] args) {
        int num1 = 300;
        int num2 = 30;
        int result = num1 + num2;
        System.out.println("num1 + num2 = " + result);

        result = num1 - num2;
        System.out.println("num1 - num2 = " + result);

        result = num1 * num2;
        System.out.println("num1 * num2 = " + result);

        result = num1 / num2;
        System.out.println("num1 / num2 = " + result);

        result = num1 % num2;
        System.out.println("num1 % num2 = " + result);




    }

}
